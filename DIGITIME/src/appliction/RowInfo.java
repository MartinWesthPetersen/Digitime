package appliction;

public class RowInfo {

	private String description;
	private String dato;
	private String starttid;
	private String sluttid;
	private String tiddifference;
	private String kroner;
	private String dispbeløb;
	
	public RowInfo() {
		
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getDato() {
		return dato;
	}

	public void setDato(String dato) {
		this.dato = dato;
	}

	public String getStarttid() {
		return starttid;
	}

	public void setStarttid(String starttid) {
		this.starttid = starttid;
	}

	public String getSluttid() {
		return sluttid;
	}

	public void setSluttid(String sluttid) {
		this.sluttid = sluttid;
	}

	public String getTiddifference() {
		return tiddifference;
	}

	public void setTiddifference(String tiddifference) {
		this.tiddifference = tiddifference;
	}

	public String getKroner() {
		return kroner;
	}

	public void setKroner(String kroner) {
		this.kroner = kroner;
	}

	public String getDispbeløb() {
		return dispbeløb;
	}

	public void setDispbeløb(String dispbeløb) {
		this.dispbeløb = dispbeløb;
	}
	
}
